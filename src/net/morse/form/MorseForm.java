package net.morse.form;

public class MorseForm {
	
	private String textIn;
	private String morseOut;
	
	
	public String getTextIn() {
		return textIn;
	}
	public void setTextIn(String textIn) {
		this.textIn = textIn;
	}
	public String getMorseOut() {
		return morseOut;
	}
	public void setMorseOut(String morseOut) {
		this.morseOut = morseOut;
	}

}
